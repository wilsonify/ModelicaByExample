from bounce import pbdesign, bbdesign, ffdesign


def test_pbdesign():
    bd = {"e": [0.1, 1.0], "g": [1.0, 10.0]}
    doe = pbdesign(bd)
    assert doe == [
        {'e': 0.0, 'g': 1.0},
        {'e': 1.0, 'g': 1.0},
        {'e': 0.0, 'g': 10.0},
        {'e': 1.0, 'g': 10.0},
    ]


def test_bbdesign():
    bd = {"e": [0.1, 1.0], "g": [1.0, 10.0]}
    doe = bbdesign(bd)
    assert doe == [
        {'e': 0.0, 'g': 1.0},
        {'e': 1.0, 'g': 1.0},
        {'e': 0.0, 'g': 10.0},
        {'e': 1.0, 'g': 10.0},
        {'e': 1.0, 'g': 5.5}
    ]


